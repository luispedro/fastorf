START_CODONS = ['ATG', 'GTG', 'TTG']
STOP_CODONS = ['TAA', 'TAG', 'TGA']

def is_start(codon):
    return codon in START_CODONS
def is_stop(codon):
    return codon in STOP_CODONS

def is_start_reverse_complement(codon):
    return codon in ['CAT', 'CAC', 'CAA']

def is_stop_reverse_complement(codon):
    return codon in ['TTA', 'CTA', 'TCA']

def reverse_complement(seq):
    return seq[::-1].translate(str.maketrans('ATGC', 'TACG'))

